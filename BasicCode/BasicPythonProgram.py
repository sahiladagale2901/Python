####################################### Find duplicate elements in a list ##############################################

l = [22, 2345, 56, 65, 56, 6587, 3, 2, 11, 11, 44]
count = 1
for i in range(len(l) - 1):
    for j in range(i+1, len(l) - 1):
        if l[i] == l[j]:
            count += 1
        break

    print(f"{l[i]} occur {count} times")
